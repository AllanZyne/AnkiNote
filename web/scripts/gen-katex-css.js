// Generates web/src/lib/katex-css.js: KaTeX's stylesheet with every woff2 font
// base64-inlined, so cards render math inside a network-less sandboxed iframe.
// Run after upgrading katex: `node scripts/gen-katex-css.js` (from web/).
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const distDir = join(here, '..', 'node_modules', 'katex', 'dist');

let css = readFileSync(join(distDir, 'katex.min.css'), 'utf8');

// Inline each woff2 as a data: URI and drop the woff/ttf fallbacks in that src.
css = css.replace(/src:url\(fonts\/([\w-]+)\.woff2\)[^;}]*/g, (_m, base) => {
  const b64 = readFileSync(join(distDir, 'fonts', `${base}.woff2`)).toString('base64');
  return `src:url(data:font/woff2;base64,${b64}) format("woff2")`;
});

const out = `// AUTO-GENERATED by scripts/gen-katex-css.js — do not edit by hand.
export const katexCss = ${JSON.stringify(css)};
`;
writeFileSync(join(here, '..', 'src', 'lib', 'katex-css.js'), out);
console.log(`Wrote katex-css.js (${(out.length / 1024).toFixed(0)} KB)`);
